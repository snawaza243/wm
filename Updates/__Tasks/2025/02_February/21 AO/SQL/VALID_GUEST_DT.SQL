
SELECT D.GUEST_CD FROM CLIENTBASICDET A, CLIENTPERSDET B, CLIENTWORKDET C, BAJAJ_VENUE_BOOKING D
WHERE  
    D.GUEST_CD  IS NOT NULL
    AND TO_CHAR(A.ACCOUNTNO) = D.PITCH_BOOK_NO
    AND A.ACCOUNTNO = B.ACCOUNTNO
    AND A.ACCOUNTNO = C.ACCOUNTNO(+)
    AND D.GUEST_CD IN (SELECT GUEST_CD FROM TB_DOC_UPLOAD)
    AND D.GUEST_CD IN (SELECT GUEST_CD FROM CLIENT_MASTER)
    ;

