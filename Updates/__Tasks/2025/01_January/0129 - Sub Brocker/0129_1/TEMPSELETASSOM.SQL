create or replace PROCEDURE PSM_AM_GET_AGENT_BY_ID(
    p_agent_id      IN  VARCHAR2,
    p_result          OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_result FOR
    SELECT 
    '' AS DT_NUMBER,
    PAYMENTMODEID, ACCTYPEID, BANKID, CITY_NAME, ACCNO, NEFT_BANK_NAME, BANK_BRANCH_NAME, IFSC_CODE, NAME_IN_BANK, AFFECTEDFROM, TIMEST, ANA_AUDITDATE, DOB, POSP_CERTIFIED_ON_GI, POSP_VALID_TILL_GI, POSP_CERTIFIED_ON_LI, POSP_VALID_TILL_LI, AMFICERT, ANA_AUDIT, ONLINE_SUBSCIPTION, BLOCK_AGENT, ONLINE_BLOCK_AGENT, VERIFIED_STATUS, CPEMAILID, SUB_BROKER_TYPE, PAIDFLAG, AGENT_NAME, EMAIL, AGENT_CODE, EXIST_CODE, SOURCEID, RM_CODE, ADDRESS1, ADDRESS2, PINCODE, CITY_ID,  LOCATION_ID, MOBILE, FAX, CONTACTPER, TDS, PHONE, REMARK, CATEGORY_ID, ONLINE_BLOCK_REMARK, OFFLINE_BLOCK_REMARK, BANK_BRANCHID, SMS_FLAG, GSTIN_NO, AGENT_TYPE, PAN, DIST, AADHAR_CARD_NO, POSP_TYPE, POSP_NO_LI, POSP_NO_GI, POSP_MARKING, DOC_ID, MASTER_ANA, R_ADDRESS1 , R_ADDRESS2 , R_STATE_NAME , R_CITY_NAME ,
    R_PINCODE 
    FROM AGENT_MASTER
    WHERE AGENT_CODE = p_agent_id;
END PSM_AM_GET_AGENT_BY_ID;