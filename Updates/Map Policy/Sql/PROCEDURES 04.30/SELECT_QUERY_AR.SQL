SELECT  DISTINCT
    A.SYS_AR_NO         AS AR_NO,
    FI.ITEMNAME         AS CHANNEL,
    R.REGION_NAME       AS REGION, 
    Z.ZONE_NAME         AS ZONE,
    B.BRANCH_NAME       AS BRANCH, 
    E.RM_NAME           AS RM_NAME, 
    D.STATUS_CD         AS STATUS,
    D.STATUS_DT         AS STATUS_DATE,
    DECODE(A.PREM_FREQ, 1, 'Y', 2, 'HY', 4, 'Q', 12, 'M') AS FREQ, 
    A.PREM_AMT          AS PREMIUM,
    A.COMPANY_CD        AS COMPANY_CD,   
    PM.PLAN             AS PLAN_NAME,
    P.POLICY_NO         AS POLICY_NO, 
    A.PAYMENT_MODE      AS PAYMENT_MODE,
    A.SA                AS SA,
    TO_CHAR(A.PLY_ISSUE_DT,'DD-MON-YYYY') AS ISSUE_DATE,
    A.FRESH_RENEWAL     AS FRR_DATE,
    PM.sub_category     AS PLAN_TYPE

FROM 
    BRANCH_MASTER B, 
    ZONE_MASTER Z, 
    POLICY_MAP_TEMP1 P, 
    BAJAJ_AR_HEAD A,
    BAJAJ_AR_DETAILS D,
    BAJAJ_PLAN_MASTER PM, 
    INVESTOR_MASTER I, 
    EMPLOYEE_MASTER E, 
    REGION_MASTER R, 
    STATE_MASTER S,
    CITY_MASTER C, 
    FIXEDITEM FI
WHERE 
    I.CITY_ID           = C.CITY_ID 
    AND C.STATE_ID      = S.STATE_ID 
    AND E.RM_CODE       = I.RM_CODE 
    AND A.SYS_AR_NO     = P.SYS_AR_NO
    AND A.CLIENT_CD     = I.INV_CODE 
    AND I.BRANCH_CODE   = B.BRANCH_CODE 
    AND B.REGION_ID     = R.REGION_ID 
    AND B.ZONE_ID       = Z.ZONE_ID
    AND A.SYS_AR_NO     = D.SYS_AR_NO(+)
    AND A.PLAN_NO       = PM.PLAN_NO
    AND B.BRANCH_TAR_CAT = FI.ITEMSERIALNUMBER
GROUP BY 
    A.SYS_AR_NO, FI.ITEMNAME, R.REGION_NAME, Z.ZONE_NAME,
    B.BRANCH_NAME, E.RM_NAME, D.STATUS_CD, D.STATUS_DT,
    A.PREM_FREQ, A.PREM_AMT, A.COMPANY_CD, PM.PLAN,
    P.POLICY_NO, A.PAYMENT_MODE, A.SA, A.PLY_ISSUE_DT,
    A.FRESH_RENEWAL, PM.sub_category;
    
    
    
    SELECT * FROM BAJAJ_AR_HEAD A, POLICY_MAP_TEMP1 P
    WHERE  A.SYS_AR_NO     = P.SYS_AR_NO;
