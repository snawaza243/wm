CREATE OR REPLACE PROCEDURE WEALTHMAKER.PSM_LOG_BANK_MASTER(
    P_FOR       IN VARCHAR2, -- 'COUNTRY_LIST', 'STATE_LIST', 'CITY_LIST'
    P_BY        IN VARCHAR2, -- 'BY_COUNTRY', 'BY_STATE', 'BY_CITY'
    P_BY_VALUE  IN VARCHAR2, -- filter value
    P_LOG_ID    IN VARCHAR2,
    P_ROLE_ID   IN VARCHAR2,
    P_CURSOR    OUT SYS_REFCURSOR
) IS

BEGIN
    OPEN P_CURSOR FOR 
        SELECT DISTINCT BANK_NAME, BANK_ID
        FROM BANK_MASTER 
        WHERE BANK_NAME IS NOT NULL 
        AND BANK_ID IS NOT NULL
        ORDER BY BANK_NAME ASC;
END PSM_LOG_BANK_MASTER;