    SELECT DISTINCT RM_NAME, PAYROLL_ID, RM_CODE, SOURCE, (SELECT BRANCH_NAME FROM BRANCH_MASTER WHERE BRANCH_CODE = SOURCE) AS BRANCH_NAME
    FROM EMPLOYEE_MASTER EM
    WHERE 1=1 AND TYPE = 'A'
    --AND SOURCE = 'BRANCH'
    AND SOURCE IN (SELECT BRANCH_ID FROM USERDETAILS_JI WHERE LOGIN_ID = '121397' AND ROLE_ID='212')
    ORDER BY RM_NAME