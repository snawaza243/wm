SELECT 
    CNTM.COUNTRY_ID AS COUNTRY_ID, 
    CNTM.COUNTRY_NAME AS COUNTRY_NAME, 
    STM.STATE_ID AS STATE_ID, 
    STM.STATE_NAME AS STATE_NAME,
    CTM.CITY_ID AS CITY_ID, 
    CTM.CITY_NAME AS CITY_NAME

FROM COUNTRY_MASTER CNTM
LEFT JOIN STATE_MASTER STM ON STM.COUNTRY_ID = CNTM.COUNTRY_ID
LEFT JOIN CITY_MASTER CTM ON CTM.STATE_ID = STM.STATE_ID

WHERE 
     CNTM.COUNTRY_ID IS NOT NULL 
    AND STM.STATE_ID IS NOT NULL
    AND STM.COUNTRY_ID IS NOT NULL 
    AND CTM.CITY_ID IS NOT NULL
    AND CTM.STATE_ID IS NOT NULL
    AND STM.DEL_FLAG IS NULL
    AND CTM.DEL_FLAG IS NULL
ORDER BY CNTM.COUNTRY_NAME, STM.STATE_NAME, CTM.CITY_NAME;
