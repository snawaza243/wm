create or replace PROCEDURE PSM_AM_BANK_BRANCH_ONLY(

p_cursor OUT SYS_REFCURSOR)
AS
BEGIN
    OPEN p_cursor FOR
        SELECT DISTINCT
            BRANCH as BRANCH_NAME
        FROM BANK_MASTER
        WHERE 
        TRIM(BRANCH) IS NOT NULL
        AND TRIM(BANK_ID) IS NOT NULL  ORDER BY BRANCH;
END;