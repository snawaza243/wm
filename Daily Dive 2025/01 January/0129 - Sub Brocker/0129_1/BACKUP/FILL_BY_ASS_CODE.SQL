BEGIN
    OPEN p_result FOR
    SELECT 
    '' AS DT_NUMBER,
    AGENT_MASTER.PAYMENTMODEID,
    AGENT_MASTER.ACCTYPEID,
    AGENT_MASTER.BANKID,
    AGENT_MASTER.CITY_NAME,
    AGENT_MASTER.ACCNO,
    AGENT_MASTER.NEFT_BANK_NAME,
    AGENT_MASTER.BANK_BRANCH_NAME,
    AGENT_MASTER.IFSC_CODE,
    AGENT_MASTER.NAME_IN_BANK,
    AGENT_MASTER.AFFECTEDFROM,
    AGENT_MASTER.TIMEST,
    AGENT_MASTER.ANA_AUDITDATE,
    AGENT_MASTER.DOB,
    AGENT_MASTER.POSP_CERTIFIED_ON_GI,
    AGENT_MASTER.POSP_VALID_TILL_GI,
    AGENT_MASTER.POSP_CERTIFIED_ON_LI,
    AGENT_MASTER.POSP_VALID_TILL_LI,
    AGENT_MASTER.AMFICERT,
    AGENT_MASTER.ANA_AUDIT,
    AGENT_MASTER.ONLINE_SUBSCIPTION,
    AGENT_MASTER.BLOCK_AGENT,
    AGENT_MASTER.ONLINE_BLOCK_AGENT,
    AGENT_MASTER.VERIFIED_STATUS,
    AGENT_MASTER.CPEMAILID,
    AGENT_MASTER.SUB_BROKER_TYPE,
