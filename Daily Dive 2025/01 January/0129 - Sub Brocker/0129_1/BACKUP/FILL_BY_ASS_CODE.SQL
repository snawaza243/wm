    AGENT_MASTER.POSP_NO_LI,
    AGENT_MASTER.POSP_NO_GI,
    AGENT_MASTER.POSP_MARKING,
    AGENT_MASTER.DOC_ID,
    AGENT_MASTER.MASTER_ANA,

    AGENT_MASTER.R_ADDRESS1 AS RES_ADD_1,
    AGENT_MASTER.R_ADDRESS2 AS RES_ADD_2,
    AGENT_MASTER.R_STATE_NAME AS RES_ADD_STATE,
    AGENT_MASTER.R_CITY_NAME AS RES_ADD_CITY,
    AGENT_MASTER.R_PINCODE AS RES_ADD_PIN

    FROM AGENT_MASTER
    WHERE AGENT_CODE = p_agent_id;
END PSM_AM_GET_AGENT_BY_ID;