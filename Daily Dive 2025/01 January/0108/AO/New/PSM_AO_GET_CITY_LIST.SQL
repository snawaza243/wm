CREATE OR REPLACE PROCEDURE WEALTHMAKER.PSM_AO_GET_CITY_LIST(
    p_cursor OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN p_cursor FOR
    SELECT CITY_ID, CITY_NAME
    FROM CITY_MASTER
    where del_flag is null 
    and CITY_ID is not null AND CITY_NAME IN (SELECT DISTINCT CITY_NAME FROM CITY_MASTER)
    order by CITY_NAME;

END PSM_AO_GET_CITY_LIST;